public class Variables {
    public static void main(String[] args) {
        // Declaração e inicialização de variáveis
        int idade = 30;
        double altura = 1.75;
        String nome = "Maria";

        // Uso das variáveis
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
    }
}
